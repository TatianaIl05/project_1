def star(x, y, a):
    t.fillcolor('purple')
    t.begin_fill()
    t.color('purple')
    t.up()
    t.setposition(x, y)
    t.down()
    for i in range (5):
        t.forward(a)
        t.left(72)
        t.forward(a)
        t.right(144)
